// The array .at() method takes an integer value and returns the item at that index, for both positive and negative integers.
// Negative integers count backwords from the last array item.
const condiments = ["ketchup", "mayonnaise", "mustard"];
const atWay = condiments.at(-3);
const bracketnotation = condiments[0];
console.log("atWay " + atWay);
console.log("bracketnotation " + bracketnotation);
